export function getRandomQuestions(
  count: number,
  filter?: {
    category?: Question["category"];
    difficulty?: Question["difficulty"];
    functionNames?: string[];
  }
): Question[] {
  let pool = [...QUESTIONS];
  if (filter?.category) pool = pool.filter((q) => q.category === filter.category);
  if (filter?.difficulty)
    pool = pool.filter((q) => q.difficulty === filter.difficulty);
  if (filter?.functionNames?.length)
    pool = pool.filter((q) =>
      filter.functionNames!.map((n) => n.toUpperCase()).includes(q.functionName.toUpperCase())
    );

  // shuffle
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, Math.min(count, pool.length));
}

/** Daily challenge: seeded by date so everyone gets same set that day */
export function getDailyQuestions(dateKey: string, count = 10): Question[] {
  let seed = 0;
  for (let i = 0; i < dateKey.length; i++) seed = (seed * 31 + dateKey.charCodeAt(i)) >>> 0;
  const pool = [...QUESTIONS];
  const rng = () => {
    seed = (seed * 1664525 + 1013904223) >>> 0;
    return seed / 0x100000000;
  };
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, Math.min(count, pool.length));
}

